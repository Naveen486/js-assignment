let addition = (num1,num2) => num1+num2;
let subtraction = (num1,num2) => num1-num2;
let multiplication=(num1,num2) => num1*num2;
let division = (num1,num2) => num1/num2;
let squareRoot = num1 => Math.sqrt(num1);
let percentage= (num1,num2) => num1%num2;

console.log(addition(10,20));
console.log(subtraction(20,10));
console.log(multiplication(10,20));
console.log(division(10,20));
console.log(squareRoot(25));
